    public function arrayKeyExists($needle, $haystack)
    {
      $result = array_key_exists($needle, $haystack);
      if ($result) return $result;
      foreach ($haystack as $v)
      {
        if (is_array($v))
        {
          $result = $this->arrayKeyExists($needle, $v);
        }
        if ($result) return $result;
      }
      return $result;
    }

    public function arraySearch($needle, $haystack)
    {
      $result = array_search($needle, $haystack);
      if ($result) return $result;
      foreach ($haystack as $v)
      {
        if (is_array($v))
        {
          $result = $this->arraySearch($needle, $v);
        }
        if ($result) return $result;
      }
      return $result;
    }

    public function getUrl()
    {
      $url = $_SERVER['QUERY_STRING'];
      if(!empty($url) && $url != '/')
      {
        $url = explode('/', $url);
      }

      return $url[0];
    }

    public function getClass($class)
    {
      $className = get_class($class);
      $className = explode("\\", $className);
      $className = str_replace('Controller', '', $className[1]);
      $className = strtolower($className);
      return $className;
    }

